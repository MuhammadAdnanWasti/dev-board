document.getElementById("com-btn-1").addEventListener("click", function (event) {
    alert("Board Updated Successfully");
    event.target.classList.add("bg-[#7781c0]");
    
    let minus=document.getElementById("minus").innerText;
    --minus;
    document.getElementById("minus").innerText=minus;
    if (minus===0) {
        alert("Congrats, you have completed all the current task");
    }
    let plus=document.getElementById("plus").innerText;
    ++plus;
    document.getElementById("plus").innerText=plus;



    let task=document.getElementById("contents");
let taskName= document.getElementById("task-1").innerText;
    let divv=document.createElement("div");
 
    divv.innerHTML=`<p class="bg-[#F4F7FF] p-3">You have completed the task ${taskName} at ${new Date()}</p>`;

   task.appendChild(divv);
   this.disabled = true;
})

